        // GET: /Account/VerifyCode
        [AllowAnonymous]
        public async Task<ActionResult> VerifyCode(string provider, string returnUrl, bool rememberMe)
        {
            // Require that the user has already logged in via username/password or external login
            if (!await SignInManager.HasBeenVerifiedAsync())
            {
                return View("Error");
            }
            return View(new VerifyCodeViewModel { Provider = provider, ReturnUrl = returnUrl, RememberMe = rememberMe });
        }

        //
        // POST: /Account/VerifyCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> VerifyCode(VerifyCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // The following code protects for brute force attacks against the two factor codes. 
            // If a user enters incorrect codes for a specified amount of time then the user account 
            // will be locked out for a specified amount of time. 
            // You can configure the account lockout settings in IdentityConfig
            var result = await SignInManager.TwoFactorSignInAsync(model.Provider, model.Code, isPersistent:  model.RememberMe, rememberBrowser: model.RememberBrowser);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(model.ReturnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid code.");
                    return View(model);
            }
        }

        //register korisnika - post
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> RegisterKorisnik(RegisterViewModelKorisnik registerViewModel)
        {
            RegisterViewModelKorisnik _registerViewModel = new RegisterViewModelKorisnik { };

            if (ModelState.IsValid)
            {
                var korisnikPostoji = from c in db.aspnetusers where c.UserName.Equals(registerViewModel.Username) select c;
                var korisnikPostojiEmail = from c in db.aspnetusers where c.Email.Equals(registerViewModel.Email) select c;


                if (korisnikPostoji.Count() != 0)
                {
                    ModelState.AddModelError(string.Empty, "Već postoji korisnik sa istim imenom.");
                    return View("~/Views/Account/RegisterKorisnik.cshtml", _registerViewModel);
                }
                else if (korisnikPostojiEmail.Count() != 0)
                {
                    ModelState.AddModelError(string.Empty, "Već postoji korisnik sa istim e-mailom.");
                    return View("~/Views/Account/RegisterKorisnik.cshtml", _registerViewModel);

                }

                var user = new ApplicationUser { UserName = registerViewModel.Username, Email = registerViewModel.Email };
                var result = await UserManager.CreateAsync(user, registerViewModel.Password);

                if (result.Succeeded)
                {
                    var roleStore = new RoleStore<IdentityRole>(new ApplicationDbContext());
                    var roleManager = new RoleManager<IdentityRole>(roleStore);
                    await roleManager.CreateAsync(new IdentityRole("Korisnik"));
                    await UserManager.AddToRoleAsync(user.Id, "Korisnik");

                    korisnik korisnik = new korisnik { idKorisnik = user.Id, korisničkoImeKorisnik = registerViewModel.Username, registracijskiEmailKorisnik = registerViewModel.Email };
                    db.korisnik.Add(korisnik);

                    await db.SaveChangesAsync();

                    var resultLogin = await SignInManager.PasswordSignInAsync(registerViewModel.Username, registerViewModel.Password, false, shouldLockout: false);
                    switch (resultLogin)
                    {
                        case SignInStatus.Success:
                            return RedirectToAction("Profil", "Korisnik");
                        case SignInStatus.Failure:
                        default:
                            ModelState.AddModelError("", "Nešto je pošlo po zlu.");
                            return RedirectToAction("Index", "Home");
                    }
                }
                else
                {
                    List<string> errori = new List<string>();
                    foreach (string errors in result.Errors)
                    {
                        errori.Add(errors);
                    }

                    foreach (string regerror in errori)
                    {
                        ModelState.AddModelError(string.Empty, regerror);
                    }
                }
                return View("~/Views/Account/RegisterKorisnik.cshtml", _registerViewModel);
            }

            return View("~/Views/Account/RegisterKorisnik.cshtml", _registerViewModel);
        }

